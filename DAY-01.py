q = input("Ask any question related to Renewable energy: ")

if q == "What is renewable energy":
    print("Renewable energy is energy obtained from natural sources that can be replenished.")

elif q == "Why is renewable energy important":
    print("Renewable energy is important because it reduces pollution and saves natural resources.")

elif q == "What are the types of renewable energy":
    print("The main types are solar, wind, hydro, geothermal, and biomass energy.")

elif q == "What is solar energy":
    print("Solar energy is energy obtained from the sun using solar panels.")

elif q == "How does solar energy work":
    print("Solar panels convert sunlight into electricity using solar cells.")

elif q == "What is wind energy":
    print("Wind energy is electricity generated using wind turbines.")

elif q == "How does wind energy work":
    print("Wind turbines spin due to wind and generate electricity.")

elif q == "What is hydropower":
    print("Hydropower is electricity generated from flowing or falling water.")

elif q == "What is geothermal energy":
    print("Geothermal energy comes from heat inside the Earth.")

elif q == "What is biomass energy":
    print("Biomass energy is produced from plant and animal waste.")

elif q == "How does renewable energy help the environment":
    print("It reduces air pollution and greenhouse gas emissions.")

elif q == "What are fossil fuels":
    print("Fossil fuels are non-renewable energy sources like coal, oil, and gas.")

elif q == "Why should we reduce fossil fuel use":
    print("Because fossil fuels cause pollution and climate change.")

elif q == "What is one disadvantage of renewable energy":
    print("Some renewable energy sources depend on weather conditions.")

elif q == "How does renewable energy help future generations":
    print("It conserves resources and provides a cleaner environment.")

